// modulo A
export type Status = "disponible" | "ocupado";

export interface ModulePhoto {
  url: string;
  alt: string;
}

export interface Module {
  id: string;
  buildingId: "A" | "B" | "C";
  number: string;
  areaM2: number;
  status: Status;
  photos: ModulePhoto[];
  description: string;
}

export interface Building {
  id: "A" | "B" | "C";
  name: string;
  totalModules: number;
  heroImage: string;
  description: string;
  tagline: string;
}

/** Espacios del portafolio que no son edificios ni módulos */
export interface PortfolioExtra {
  id: "parqueos" | "exteriores";
  name: string;
  heroImage: string;
  tagline: string;
  description: string;
  photos: ModulePhoto[];
}

/** Genera un set de fotos placeholder para un módulo (usa imagen real del cliente) */
const fotos = (number: string, count = 4): ModulePhoto[] =>
  Array.from({ length: count }, (_, i) => ({
    url: plazaImage,
    alt: `Foto ${i + 1} del módulo ${number}`,
  }));

/** Convierte URLs importadas en el array de fotos del módulo */
const modulePhotos = (number: string, urls: string[]): ModulePhoto[] =>
  urls.map((url, i) => ({
    url,
    alt: `Foto ${i + 1} del módulo ${number}`,
  }));

